async function getRandomQuote() {
  return new Promise((resolve, reject) => {
    http.get(`${API_BASE}/api/quotes/random`, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try {
          const json = JSON.parse(data);
          resolve(json);
        } catch (e) {
          reject(e);
        }
      });
    }).on('error', reject);
  });
}

